struct AlphaConstant {
    /// .zero
    static let clear: CGFloat = .zero
    
    /// 0.3
    static let veryLight: CGFloat = 0.3
    
    /// 0.5
    static let light: CGFloat = 0.5
    
    /// 0.8
    static let semiOpaque: CGFloat = 0.8
    
    /// 1
    static let opaque: CGFloat = 1
}
